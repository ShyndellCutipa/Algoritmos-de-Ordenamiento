// Implementación de SelectionSort 
#include <iostream>
using namespace std; 
  
void intercambio(int *xp, int *yp)  
{  
    int temp = *xp;  
    *xp = *yp;  
    *yp = temp;  
}  
  
void selectionSort(int arr[], int n)  
{  
    int i, j, min_idx;  
  
    for (i = 0; i < n-1; i++)  
    {  
        // Se encuentra el mínimo elemento
        min_idx = i;  
        for (j = i+1; j < n; j++)  
        if (arr[j] < arr[min_idx])  
            min_idx = j;  
  
        // Intercambiar el elemento mínimo encontrado con el primer elemento  
        intercambio(&arr[min_idx], &arr[i]);  
    }  
}  

void ImprimirArray(int arr[], int size)  
{  
    int i;  
    for (i=0; i < size; i++)  
        cout << arr[i] << " ";  
    cout << endl;  
}  
  
 
int main()  
{  
    int arr[] = {64, 25, 12, 22, 11};  
    int n = sizeof(arr)/sizeof(arr[0]);  
    selectionSort(arr, n);  
    cout << "Arreglo ordenado: \n";  
    ImprimirArray(arr, n);  
    return 0;  
} 